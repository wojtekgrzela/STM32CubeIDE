void Start50msTask(void const *argument)
{
	TickType_t xLastWakeTime;
	const TickType_t xFrequency = MY_TASK_50_MS_TIME_PERIOD;

	volatile uint16_t TIM1CounterReadout = 0;
	static uint16_t previousCounterValue = 0;
	volatile int32_t tempDiff = 0;

	xLastWakeTime = xTaskGetTickCount();

	/* Infinite loop */
	/* * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * */
	for (;;)
	{
		TIM1CounterReadout = __HAL_TIM_GET_COUNTER(&htim1);
		tempDiff = TIM1CounterReadout - previousCounterValue;

		if ((4 <= tempDiff) || (-4 >= tempDiff))
		{
			tempDiff /= 4;
			EncoderCounterDiff += (int8_t)tempDiff;
			previousCounterValue = TIM1CounterReadout;
		}

		vTaskDelayUntil(&xLastWakeTime, xFrequency);
	}
	/* * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * * */
}
